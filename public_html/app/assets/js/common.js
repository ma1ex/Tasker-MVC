$(function() {

// -----------------------------------------------------------------------------
// === GLOBAL VARS =============================================================
// -----------------------------------------------------------------------------
	


// -----------------------------------------------------------------------------
// === MAIN ====================================================================
// -----------------------------------------------------------------------------


// === OWL CAROUSEL ===
	var owlBrands = $('.owl-carousel');
	owlBrands.owlCarousel({
		margin: 10,
		nav: true,
		navContainer: "#owl-nav-brands",
		loop: true,
		autoplay: true,
		autoplayTimeout: 3000,
		autoplayHoverPause: true,
		navText: ["<i class='fa fa-chevron-left'></i>", "<i class='fa fa-chevron-right'></i>"],
		navElement: "a",
		// navClass: ['owl-prev', 'owl-next']
		responsive: {
			0: {
				items: 1
			},
			360: {
				items: 2
			},
			600: {
				items: 5
			},
			960: {
				items: 6
			},
			1200: {
				items: 7
			}
		}
	});
	
	owlBrands.on('mousewheel', '.owl-stage', function(e) {
		if (e.deltaY > 0) {
			owlBrands.trigger('next.owl');
		} else {
			owlBrands.trigger('prev.owl');
		}
		e.preventDefault();
	});
// === /OWL CAROUSEL ====



// === /MAIN ===================================================================
// -----------------------------------------------------------------------------


	

	

// -----------------------------------------------------------------------------
// === HELPER FUNCTIONS ========================================================
// -----------------------------------------------------------------------------



// -----------------------------------------------------------------------------
// === TEMP ====================================================================
// -----------------------------------------------------------------------------


// === /TEMP ===================================================================
// -----------------------------------------------------------------------------

// === LOADERS ====

	// $(window).resize();
	// $(window).scroll();
});